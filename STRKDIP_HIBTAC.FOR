	SUBROUTINE STRKDIP_HIBTAC(HE,ME,LE,IPRT)

C	***********************************************************
C	* THIS SUBROUTINE COMPUTES THE STRIKE AND DIP FOR A GIVEN *
C	* LOCATION USING THE 3 CLOSEST DRILL HOLES                *
C	* "HE", "ME", AND "LE"  REFER TO THE HIGH, MIDDLE, AND    *
C	* LOW ELEVATION DRILL HOLE LOCATORS  USED IN THE STRIKE   *
C	* AND DIP CALCULATIONS                                    *
C	* "CL" IS THE COMMON LAYER LOCATOR SELECTED FROM AMONG    *
C	* LC1,LC3 AND LS LAYERS                                   *
C	***********************************************************

	IMPLICIT	NONE
	COMMON		/SUBS/STKS,STKD,DIPS,DIPD,DIR,CL
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2	N,HE,ME,LE,CL,IPRT,CD
	REAL*4		TANA,ANGA,BLMC,SQR,LM,LC,LB,BC,CEW,CNS,STKS,
	1		STKD,ANGB,DIPS,DIPD,ANGC,M1,M2,B1,B2,INS,DIR,
	2		LI,DHS(10,25),ECH,ECHHD

C	* LET "LMC" BE A LINE DRAWN FROM THE LOW ELEVATION  *
C	* HOLE "L" THROUGH THE MIDDLE ELEVATION HOLE "M" TO *
C	* A POINT "C" WHICH IS EQUAL IN ELEVATION TO THE    *
C	* HIGH ELEVATION HOLE "H"

C	* STEP1 - COMPUTE THE BEARING OF "LMC" (MEASURED *
C	* CLOCKWISE FROM NORTH) *

	TANA=(DHS(3,ME)-DHS(3,LE))/(DHS(2,ME)-DHS(2,LE)+.01)
	ANGA=ATAND(TANA)	! RESULT IS IN DEGREES 
	BLMC=90.-ANGA		! BEARING LMC IN DEGREES

C	* STEP2 - COMPUTE COORDINATES OF POINT "C" *

	SQR=(DHS(2,ME)-DHS(2,LE))**2+(DHS(3,ME)-DHS(3,LE))**2
	LM=SQRT(SQR)		! LENGTH OF "LM"

C	* REFER TO A VERTICAL SECTION DRAWN THROUGH LINE "LMC" *
C	* TO COMPUTE LENGTH OF "LC"                            *

	LC=LM*(DHS(CL,HE)-DHS(CL,LE))/(DHS(CL,ME)-DHS(CL,LE)+.01)

C	* USE SIMILAR TRIANGLES TO COMPUTE LENGTHS OF "LB" AND "BC" *

	IF(LM.EQ.0.)LM=.001		! INSURE THAT "LM" IS NOT ZERO
	LB=LC*(DHS(2,ME)-DHS(2,LE))/LM	! LENGTH OF "LB"
	BC=LC*(DHS(3,ME)-DHS(3,LE))/LM	! LENGTH OF "BC"
	CEW=DHS(2,LE)+LB		! E-W COORDINATE OF "C"
	CNS=DHS(3,LE)+BC		! N-S COORDINATE OF "C"

C	* LET "HC" BE A LINE DRAWN THROUGH POINTS "H" AND "C" *
C	* "HC" IS THE STRIKE FOR THE 3 SELECTED DRILL HOLES   *

C	* STEP3 - COMPUTE THE BEARING OF "HC" (MEASURED CLOCKWISE *
C	* FROM NORTH)                                             *
C	* "STKS" IS THE STRIKE SLOPE AND "STKD" IS THE BEARING OF *
C	* OF THE STRIKE IN DEGREES                                *

	STKS=(CNS-DHS(3,HE))/(CEW-DHS(2,HE)+.01)
	ANGB=ATAND(STKS)		! RESULT IN DEGREES
	STKD=90.-ANGB			! BEARING OF STRIKE IN DEGREES

C	* STEP4 - COMPUTE "DIPS" AND "DIPD" (DIP SLOPE AND DIP *
C	* IN DEGREES) MEASURED IN DEGREES FROM THE HORIZONTAL  *
C	* DRAW A LINE FROM THE LOW HOLE "L" PERPENDICULAR TO   *
C	* THE STRIKE LINE "HC" INTERSECTING IT AT A POINT "I"  *

	IF((STKD-BLMC).LE.90.)ANGC=ABS(STKD-BLMC)
	IF((STKD-BLMC).GT.90.)ANGC=ABS(180.-STKD+BLMC)
	LI=SIND(ANGC)*LC		! LENGTH OF "LI"
	IF(LI.EQ.0.)LI=.001		! INSURE THAT "LI" IS NOT ZERO

C	* REFER TO VERTICAL SECTION DRAWN THROUGH LINE "LI" *

	DIPS=(DHS(CL,HE)-DHS(CL,LE))/LI	! DIP SLOPE
	DIPD=ATAND(DIPS)		! DIP IN DEGREES

C	* STEP5 - COMPUTE THE NORTH COORDINATE OF POINT "I" USING *
C	* LINE INTERSECTION FORMULAS AND LINES "IHC" (LINE1) AND  *
C	* "LI" (LINE2)                                            *

	IF(STKS.EQ.0.)STKS=.001		! INSURE THAT STRIKE SLOPE > 0
	M1=STKS				! M1 IS SLOPE STKS
	M2=-(1./STKS)			! M2 IS PERPENDICULAR TO M1
	B1=DHS(3,HE)-(M1*DHS(2,HE))	! Y INTERCEPT FOR M1
	B2=DHS(3,LE)-(M2*DHS(2,LE))	! Y INTERCEPT FOR M2
	INS=(B1*M2-B2*M1)/(M2-M1)	! N-S COORDINATE OF "I"
	DIR=1.				! ASSUME DIP IS UP TO NORTH
	IF(DHS(3,LE).GE.INS)DIR=2.	! DIP IS DOWN TO NORTH

C	* IF "IPRT" IS 2 OR 3, PRINT COMPUTATIONS AND RESULTS OF *
C	* STRIKE/DIP SUBROUTINE                             *

	IF(IPRT.EQ.2)THEN

	  PRINT 10,TANA,ANGA,BLMC,LM,LC,LB,BC,CEW,CNS,STKS,ANGB,STKD,
	1ANGC,LI,DIPS,DIPD,M1,M2,B1,B2,INS,DIR
10	FORMAT(1X///5X,'STRIKE AND DIP SUBROUTINE COMPUTATIONS ARE'//5X,
	1'TANA  ANGA    BLMC       LM         LC         LB         BC '
	2,'       CEW',8X,'CNS     STKS   ANGB    STKD'/5X,'----  ---- '
	3,'  -----     ------     ------     ------     ------   ------'
	4,'--   --------  -----  -----  ------'/1X,F8.1,F6.1,F8.1,6F11.1
	5,2F7.1,F8.1//5X,'ANGC     LI      DIPS     DIPD      M1       '
	6,'M2',8X,'B1',10X,'B2',9X,'INS       DIR'/5X,'----    ----    '
	7,' ----     ----     ----     ----     ------      ------     '
	8,' -----      ---'/4X,F5.1,2X,F7.1,F8.1,3F9.1,3F12.1,5X,F2.0)

	ELSE IF(IPRT.EQ.3)THEN

	  WRITE (88,20)TANA,ANGA,BLMC,LM,LC,LB,BC,CEW,CNS,STKS,ANGB,STKD
	1,ANGC,LI,DIPS,DIPD,M1,M2,B1,B2,INS,DIR
20	FORMAT(1X///5X,'STRIKE AND DIP SUBROUTINE COMPUTATIONS ARE'//5X,
	1'TANA  ANGA    BLMC       LM         LC         LB         BC '
	2,'       CEW',8X,'CNS     STKS   ANGB    STKD'/5X,'----  ---- '
	3,'  -----     ------     ------     ------     ------   ------'
	4,'--   --------  -----  -----  ------'/1X,F8.1,F6.1,F8.1,6F11.1
	5,2F7.1,F8.1//5X,'ANGC     LI      DIPS     DIPD      M1       '
	6,'M2',8X,'B1',10X,'B2',9X,'INS       DIR'/5X,'----    ----    '
	7,' ----     ----     ----     ----     ------      ------     '
	8,' -----      ---'/4X,F5.1,2X,F7.1,F8.1,3F9.1,3F12.1,5X,F2.0)

	END IF

	RETURN
	END
