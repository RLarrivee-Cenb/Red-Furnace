	SUBROUTINE GENERAL_FORMAT(IN,OUT,PLACES)

C	*************************************************************
C	* THIS SUBROUTINE CONVERTS NUMERIC DATA TO CHARACTER STRING *
C	* WITH COMMAS, AND DECIMAL POINT IF PLACES IS > 0           *
C	*************************************************************

	IMPLICIT	NONE
	INTEGER*2	PLACES,J,K,N,KS,L,LS
	REAL*8		IN
	CHARACTER	OUT*20,WORK*16

	K=0
	N=0
	WORK='                '
	OUT='                    '

	IF(PLACES.EQ.0)THEN
	  WRITE (WORK,100)IN
100	  FORMAT(F16.0)
	  KS=16
	  LS=21
	ELSE IF(PLACES.EQ.1)THEN
	  WRITE (WORK,110)IN
110	  FORMAT(F16.1)
	  KS=15
	  LS=19
	ELSE IF(PLACES.EQ.2)THEN
	  WRITE (WORK,120)IN
120	  FORMAT(F16.2)
	  KS=14
	  LS=18
	ELSE IF(PLACES.EQ.3)THEN
	  WRITE (WORK,130)IN
130	  FORMAT(F16.3)
	  KS=13
	  LS=17
	ELSE IF(PLACES.EQ.4)THEN
	  WRITE (WORK,140)IN
140	  FORMAT(F16.4)
	  KS=12
	  LS=16
	ELSE IF(PLACES.EQ.5)THEN
	  WRITE (WORK,150)IN
150	  FORMAT(F16.5)
	  KS=11
	  LS=15
	ELSE IF(PLACES.GE.6)THEN
	  WRITE (WORK,160)IN
160	  FORMAT(F16.6)
	  PLACES=6
	  KS=10
	  LS=14
	END IF

	L=LS
	DO K=KS,1,-3
	DO J=1,3
	L=L-1
	OUT(L:L)=WORK(K-J:K-J)
	IF(OUT(L:L).EQ.' ')GO TO 300
	END DO
	IF(WORK(K-4:K-4).NE.' ')THEN
	  L=L-1
	  OUT(L:L)=','
	END IF
	END DO

300	IF(PLACES.NE.0)THEN

	  DO J=LS,20
	  OUT(J:J)=WORK(J-4:J-4)
	  END DO

	END IF

	RETURN
	END
