	SUBROUTINE SEL3HLE_HIBTAC(FILE_TYPE,JERR,MODE)

C	**************************************************************
C	* THIS SUBROUTINE SELECTS THE 3 CLOSEST HOLES TO THE CURRENT *
C	* SHOVEL LOCATION TO USE IN COMPUTING THE STRIKE AND DIP     *
C	* THE 3 HOLES MUST HAVE AT LEAST 1 LAYER IN COMMON AMONG THE *
C	* (LS,LC3,LC1) LAYERS AND ALL 3 HOLES SELECTED CAN NOT BE ON *
C	* THE SAME SECTION                                           *
C	**************************************************************

	IMPLICIT	NONE
	EXTERNAL	DSC$K_DTYPE_F
	COMMON		/SUBS/STKS,STKD,DIPS,DIPD,DIR,CL
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2	N,CD,CD1,CD2,J,K,L,MD,FD,HE,ME,LE
	INTEGER*2	JERR,KEYBUF(5)/1,0,0,28,4/,CL,IPRT/3/
	INTEGER*4	FLAGSUB/64/
	REAL*4		DHS(10,25),XCD,XCD1,XCD2,CLAY,DIS1,DIS2,DIS3,
	1		DIS4,TOTDIST,DUMMY(4),STKS,STKD,DIPS,DIPD,DIR,
	2		SHVL,EWN,EWA,NSN,NSA,ELEV,HD,ECH,ECHHD
	REAL*4		SLOPE1,SLOPE2,SLOPE3
	CHARACTER	FILE_TYPE*4,FILEIN9*24,FILEIN10*25,DELETE9*33,
	1		DELETE10*34,MODE*5

	CD=0			! ZERO OUT CLOSEST HOLE LOCATOR

	FILEIN9='HIBTAC:SEL3HLE_'//FILE_TYPE//'.TPE9'
	FILEIN10='HIBTAC:SEL3HLE_'//FILE_TYPE//'.TPE10'

	OPEN(9,FILE=FILEIN9,STATUS='NEW',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=8)

C	* 1) BEGINNING WITH THE CLOSEST HOLE IN THE "DHS" ARRAY, FIND *
C	*    ALL THE COMBINATIONS OF THAT HOLE WITH THE REMAINING 24  *
C	*    HOLES TAKEN 3 AT A TIME                                  *
C	* 2) DETERMINE THE TOTAL DISTANCE FROM THE SHOVEL OF THE 3    *
C	*    HOLES IN EACH GROUP (DIST 1 + DIST 2 + DIST 3)           *
C	* 3) WRITE THE 3 HOLES IN GROUP PLUS TOTAL DISTANCE FOR GROUP *
C	*    FOR ALL THE 3 HOLE GROUPS IN "DHS" TO TAPE9              *
C	* 4) RETURN TO STEP 1), TAKE THE NEXT CLOSEST HOLE IN THE     *
C	*    "DHS" ARRAY AND REPEAT STEPS (1-3) UNTIL ALL 25 HOLES    *
C	*    HAVE BEEN CYCLED                                         *
C	* 5) SORT TAPE9 BY TOTAL DISTANCE FOR EACH 3 HOLE GROUP AND   *
C	*    WRITE OUTPUT TO TAPE10                                   *
C	* 6) CHECK EACH 3 HOLE GROUP IN CLOSEST TOTAL DISTANCE ORDER  *
C	*    UNTIL A GROUP IS FOUND THAT SATISFIES THE COMMON LAYER   *
C	*    AND SECTION REQUIREMENTS                                 *

10	CD=CD+1			! INCREMENT CLOSEST HOLE COUNTER

	WRITE (88,11)CD
11	FORMAT(1X,'SEL3HLE_HIBTAC  -  CD (CLOSEST HOLE COUNTER) AFTER S'
	1,'NO 10 = ',I2)

C	* CHECK CLOSEST HOLE TO INSURE THAT IT HAS AT LEAST *
C	* 1 OF THE 3 ACCEPTABLE LAYERS (LS,LC3,LC1)         *

	DO 20 K=6,8
	IF(DHS(K,CD).GT.0.)GO TO 30
20	CONTINUE
	GO TO 10

C	* SET AND INCREMENT CLOSEST HOLE + 1 *
C	* AND CLOSEST HOLE + 2 COUNTERS      *

30	CD1=CD
40	CD1=CD1+1
	CD2=CD1+1 

	WRITE (88,41)CD1,CD2
41	FORMAT(1X/1X,'SEL3HLE_HIBTAC  -  SNO 30-40  -  CD1 (CLOSEST HOL'
	1,'E COUNTER + 1) = ',I2,2X,'CD2 (CLOSEST HOLE COUNTER + 2) = ',
	2I2)

	DIS1=ABS(DHS(2,CD)-DHS(2,CD1))	! E-W DIS BETWEEN HOLE1 & NEXT 
	DIS3=ABS(DHS(3,CD)-DHS(3,CD1))	! N-S DIS BETWEEN HOLE1 & NEXT

C	* EVALUATE HOLES IN GROUPS OF 3 AND WRITE TO TAPE9 *

	WRITE (88,42)
42	FORMAT(1X/1X,'3 HOLE GROUPS SELECTED FOR CURRENT BH ARE:'/1X,41(
	1'-'))

	DO 100 J=CD2,N

C	* DETERMINE IF 3 HOLE GROUP IS TOO CLOSE IN E-W *
C	* OR N-S DIRECTION TO COMPUTE PROPER STRIKE/DIP    *

	DIS2=ABS(DHS(2,CD)-DHS(2,J))	! E-W DIS BETWEEN HOLES 1 & J
	IF(DIS1.LE.150..AND.DIS2.LE.150.)GO TO 100

	DIS4=ABS(DHS(3,CD)-DHS(3,J))	! N-S DIS BETWEEN HOLES 1 & J
	IF(DIS3.LT.100..AND.DIS4.LT.100.)GO TO 100

C	* IF 3 HOLES FORM A STRAIGHT LINE, THEY CANNOT *
C	* FORM A UNIQUE PLANE AND MUST BE SKIPPED *

	IF((DHS(2,CD1)-DHS(2,CD)).EQ.0.)THEN
	  SLOPE1=100000.
	ELSE
	  SLOPE1=(DHS(3,CD1)-DHS(3,CD))/(DHS(2,CD1)-DHS(2,CD))
	END IF

	IF((DHS(2,J)-DHS(2,CD1)).EQ.0.)THEN
	  SLOPE2=100000.
	ELSE
	  SLOPE2=(DHS(3,J)-DHS(3,CD1))/(DHS(2,J)-DHS(2,CD1))
	END IF

	IF((DHS(2,J)-DHS(2,CD)).EQ.0.)THEN
	  SLOPE3=100000.
	ELSE
	  SLOPE3=(DHS(3,J)-DHS(3,CD))/(DHS(2,J)-DHS(2,CD))
	END IF

	IF(SLOPE1.EQ.SLOPE2.AND.SLOPE2.EQ.SLOPE3)GO TO 100

C	* DETERMINE IF 3 HOLE GROUP HAS ONE OF THE 3 *
C	* ACCEPTABLE LAYERS (LS,LC3,LC1) IN COMMON   *
C	* BEGINNING WITH LC1 AND ASSIGN COMMOM LAYER *
C	* NUMBER (CLAY) TO GROUP                     *

	DO 50 L=8,6,-1
	IF(DHS(L,CD).GT.0..AND.DHS(L,CD1).GT.0..AND.DHS(L,J).GT.0.)THEN
	  CLAY=FLOATI(L)	! SELECTED 3 HOLE GROUP LAYER LOCATOR
	  GO TO 60
	END IF
50	CONTINUE
	GO TO 100

60	TOTDIST=DHS(10,CD)+DHS(10,CD1)+DHS(10,J)
	XCD=FLOATI(CD)
	XCD1=FLOATI(CD1)
	XCD2=FLOATI(J)

C	* WRITE 3 HOLE GROUP LOCATORS, HOLE #'S AND TOTAL *
C	* DISTANCE VALUE TO TAPE 9                        *

	WRITE (9)XCD,XCD1,XCD2,CLAY,DHS(1,CD),DHS(1,CD1),DHS(1,J),
	1TOTDIST 

	WRITE (88,70)CD,DHS(1,CD),CD1,DHS(1,CD1),J,DHS(1,J),CLAY,TOTDIST
70	FORMAT(1X,'DHS(1,',I3,') = ',F6.0,2X,'DHS(1,',I3,') = ',F6.0,2X,
	1'DHS(1,',I3,') = ',F6.0,2X,'LAYER # = ',F3.0,2X,'TOTDIST = ',F7
	2.0)

100	CONTINUE

	IF(CD1.LT.(N-1))GO TO 40
	IF(CD.LT.(N-2))GO TO 10

	CLOSE(9)

	KEYBUF(2)=%LOC(DSC$K_DTYPE_F)		! DISTANCE KEY

C	* SORT TAPE9 BY TOTAL DISTANCE AND WRITE OUTPUT TO TAPE10 *

	CALL SOR$PASS_FILES(FILEIN9,FILEIN10)
	CALL SOR$BEGIN_SORT(KEYBUF,,,,,,,)
	CALL SOR$SORT_MERGE()
	CALL SOR$END_SORT()

	OPEN(10,FILE=FILEIN10,STATUS='OLD',FORM='UNFORMATTED',RECORDTYPE
	1='FIXED',RECL=8)

C	* READ SORTED 3 HOLE GROUPS FROM TAPE10 BEGINNING WITH *
C	* CLOSEST DISTANCE GROUP AND ATTEMPT TO COMPUTE STRIKE *
C	* AND DIP FOR SHOVEL LOCATION                          *

200	READ (10,END=300)XCD,XCD1,XCD2,CLAY,DUMMY

C	* ASSIGN CLOSEST,MIDDLE, FAR DISTANCE AND *
C	* LAYER LOCATORS FOR HOLES IN GROUP       *

	CD=IIFIX(XCD)			! CLOSEST HOLE IN GROUP
	MD=IIFIX(XCD1)			! MIDDLE HOLE IN GROUP
	FD=IIFIX(XCD2)			! FARTHEST HOLE IN GROUP      
	CL=IIFIX(CLAY)			! COMMON LAYER LOCATOR

C	* ASSIGN LOW ELEVATION(LE), MIDDLE ELEVATION(ME), AND *
C	* HIGH ELEVATION(HE) LOCATORS FOR HOLES IN GROUP      *

	IF(DHS(CL,FD).GE.DHS(CL,CD).AND.DHS(CL,FD).GE.DHS(CL,MD))THEN

	  HE=FD				! HIGH ELEV IS ON FARTHEST HOLE

	  IF(DHS(CL,MD).GE.DHS(CL,CD))THEN
	    ME=MD
	    LE=CD
	  ELSE
	    ME=CD
	    LE=MD
	  END IF

	ELSE IF(DHS(CL,MD).GE.DHS(CL,CD).AND.DHS(CL,MD).GE.DHS(CL,FD))
	1THEN

	  HE=MD				! HIGH ELEV IS ON MIDDLE HOLE

	  IF(DHS(CL,FD).GE.DHS(CL,CD))THEN
	    ME=FD
	    LE=CD
	  ELSE
	    ME=CD
	    LE=FD
	  END IF

	ELSE

	  HE=CD				! HIGH ELEV IS ON CLOSEST HOLE

	  IF(DHS(CL,FD).GE.DHS(CL,MD))THEN
	    ME=FD
	    LE=MD
	  ELSE
	    ME=MD
	    LE=FD
	  END IF

	END IF

C	* CALL "STRKDIP_HIBTAC" SUBROUTINE TO COMPUTE STRIKE *
C	* AND DIP FOR CURRENT SHOVEL LOCATION         *

	CALL STRKDIP_HIBTAC(HE,ME,LE,IPRT)

	IF(DIPD-15.)500,500,200		! REPEAT PROCESS IF DIP > 15 DEG

300	IF(MODE.EQ.'BATCH')THEN
	  WRITE (99,310)SHVL,EWN,EWA,NSN,NSA,ELEV,HD
310	  FORMAT(1X/5X,'CANNOT FIND 3 HOLES WITH COMMON LAYER TO COMPUT'
	1,'E STRIKE FOR SHVL ENTRY ',A3,1X,F7.0,A1,F6.0,A1,F6.0,F4.0)
	ELSE
	  PRINT 310,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	END IF
	JERR=1

C	* CLOSE AND DELETE UNITS 9 AND 10 *

500	CLOSE(9)
	CLOSE(10)
	DELETE9='DELETE '//FILEIN9//';*'
	CALL LIB$SPAWN(DELETE9,,,FLAGSUB)
	DELETE10='DELETE '//FILEIN10//';*'
	CALL LIB$SPAWN(DELETE10,,,FLAGSUB)

	RETURN
	END
