	SUBROUTINE COMPUTE_WTREC_POST_FLOAT(CWR,AWRN,CSL,MAGFE)

C	*********************************************************
C	* THIS SUBROUTINE COMPUTES CWR AND AWRN THRU COMPLETION *
C	* OF THE FLOTATION PROCESS                              *
C	* FORMULAS USED WERE DEVELOPED BY CONCENTRATOR PERSONNEL*
C	*********************************************************

	IMPLICIT	NONE
	REAL*4		FACTOR1,FACTOR2,FACTOR3,PCT_TAILS,LOSSES,
	1		CRUDE_H2O,CWR,AWRN,CSL,MAGFE,FLOAT_WR_FAC,
	2		STEP1,STEP2,STEP3,STEP4,STEP5,STEP6

	CWR=0.
	AWRN=0.

C	* ASSIGN VALUES TO FIXED FACTORS *

	FACTOR1=69.942
	FACTOR2=.8435
	FACTOR3=.9171
	PCT_TAILS=2.00
	LOSSES=.50
	FLOAT_WR_FAC=95.0
	CRUDE_H2O=2.00

C	* COMPUTE MAG IRON IN CONCENTRATE BEFORE FLOTATION *

	STEP1=FACTOR1-FACTOR2*CSL

C	* COMPUTE DRY CONCENTRATE WT REC BEFORE FLOTATION *

	STEP2=(MAGFE-PCT_TAILS)/(STEP1-PCT_TAILS)*100.

C	* SUBTRACT WEIGHT RECOVERY LOSSES TO SEWERS AND ERICKSON LAKE *

	STEP3=STEP2-LOSSES

C	* APPLY FLOTATION FEED WEIGHT RECOVERY *
C	* FACTOR TO PRE FLOAT CWR TO GET POST FLOAT CWR *

	STEP4=STEP3*FLOAT_WR_FAC*.01

	IF(STEP4.LE.0.)STEP4=.1

C	* COMPUTE DRY CRUDE REQUIRED *

	STEP5=FACTOR3/STEP4*100.

C	* COMPUTE NATURAL CRUDE REQUIRED *

	STEP6=STEP5/(100.-CRUDE_H2O)*100.

C	* COMPUTE NATURAL PELLET WT REC *

	AWRN=100./STEP6
	IF(AWRN.LE.0.)AWRN=.1

	CWR=STEP2
	IF(CWR.LE.0.)CWR=.1

100	RETURN
	END
