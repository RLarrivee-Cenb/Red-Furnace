	SUBROUTINE CHINCPT_HIBTAC(KERR,MODE)

C	**************************************************************
C	* THIS SUBROUTINE PROJECTS THE SHOVEL ELEVATION ALONG THE    *
C	* STRIKE/DIP TO DETERMINE THE ELEVATION AND FOOTAGE INTERVAL *
C	* AT WHICH THE SHOVEL PROJECTION INTERCEPTS THE CLOSEST      *
C	* DRILL HOLE                                                 *
C	**************************************************************

	IMPLICIT	NONE
	COMMON		/SUBS/STKS,STKD,DIPS,DIPD,DIR,CL
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2       CD,KERR,N,CL
	REAL*4		SHVL,EWN,EWA,NSN,NSA,ELEV,HD,DHS(10,25),M1,
	1		M2,STKS,STKD,DIPS,DIPD,DIR,B1,B2,IEW,INS,IEL,
	2		DEW,DNS,SLD,DELV,ECH,ECHHD,FTOP,FBOT
	CHARACTER	MODE*5

C	*********************************************************
C	*			REVISIONS			*
C	*			---------			*
C	* 01/14/1997 - CHANGED CLOSEST HOLE INTERCEPT 	        *
C	*	       REQUIREMENTS TO INSURE THAT INTERCEPT    *
C	*	       PLUS BANK HEIGHT ELEVATION ON CLOSEST    *
C	*	       HOLE IS AT LEAST 5' ABOVE THE BOTTOM OF  *
C	* 	       THE CLOSEST HOLE ELEVATION               *
C	*********************************************************

C	* DETERMINE THE COORDINATES OF THE POINT OF INTERSECTION *
C	* OF A LINE(L1) DRAWN THROUGH THE SHOVEL LOCATION AT THE *
C	* STRIKE SLOPE WITH A LINE(L2) DRAWN PERPENDICULAR TO IT *
C	* AND PASSING THROUGH THE CLOSEST DRILL HOLE LOCATION    *

	M1=STKS				! SLOPE OF L1
	M2=-(1./STKS)			! SLOPE OF L2
	B1=NSN-(M1*EWN)			! Y INTERCEPT FOR L1
10	B2=DHS(3,CD)-(M2*DHS(2,CD))	! Y INTERCEPT FOR L2
	IEW=(B2-B1)/(M1-M2)		! EW INTERSECTION COORDINATE
	INS=(B1*M2-B2*M1)/(M2-M1)	! NS INTERSECTION COORDINATE

C	* DETERMINE STRAIGHT LINE DISTANCE FROM CLOSEST DRILL *
C	* DRILL HOLE TO L1/L2 INTERSECTION POINT              *

	DEW=DHS(2,CD)-IEW	! EW DISTANCE CDH TO INTERSECTION
	DNS=DHS(3,CD)-INS	! NS DISTANCE CDH TO INTERSECTION
	SLD=SQRT(DEW**2+DNS**2)	! STRAIGHT LINE DIST CDH TO INTERSECTION

C	* DETERMINE CHANGE IN ELEVATION ALONG DIP FROM   *
C	* CLOSEST DRILL HOLE TO L1/L2 INTERSECTION POINT *

	DELV=DIPS*SLD

C	* DETERMINE ELEVATION(IEL) OF L1/L2 INTERSECTION POINT *

	IF(INS.GE.DHS(3,CD).AND.DIR.EQ.1.)IEL=DHS(CL,CD)+DELV
	IF(INS.GE.DHS(3,CD).AND.DIR.EQ.2.)IEL=DHS(CL,CD)-DELV
	IF(INS.LT.DHS(3,CD).AND.DIR.EQ.1.)IEL=DHS(CL,CD)-DELV
	IF(INS.LT.DHS(3,CD).AND.DIR.EQ.2.)IEL=DHS(CL,CD)+DELV

C	* DETERMINE THE CLOSEST HOLE SHOVEL INTERCEPT ELEVATION WHICH  *
C	* WILL BE THE ELEVATION AT THE CLOSEST HOLE SELECTED LAYER PLUS*
C	* THE (SHOVEL ELEVATION - L1/L2 INTERSECTION POINT ELEVATION)  *
C	* DIFFERENCE                                                   *

	ECH=AINT(DHS(CL,CD)+(ELEV-IEL)+.5)

C	* IF ELEVATION ON CLOSEST HOLE VIA SHOVEL PROJECTION *
C	* INTERCEPT IS GREATER THAN ITS COLLAR ELEVATION, GO *
C	* TO NEXT CLOSEST HOLE AND REPEAT SHOVEL PROJECTION  *
C	* INTERCEPT PROCESS                                  *

	IF(ECH.GT.DHS(4,CD))GO TO 100

C	* COMPUTE FOOTAGE ELEVATION AT TOP OF BANK ON CLOSEST HOLE *

	ECHHD=ECH+HD

C	* IF ELEVATION ON CLOSEST HOLE PLUS HOLE DEPTH VIA    *
C	* SHOVEL PROJECTION INTERCEPT IS LESS THAN 5' ABOVE   *
C	* ITS BOTTOM OF HOLE ELEVATION, GO TO THE NEXT CLOSEST*
C	* HOLE AND REPEAT SHOVEL PROJECTION INTERCEPT PROCESS *

	IF(ECHHD.LT.(DHS(9,CD)+5.))GO TO 100

C	* COMPUTE FOOTAGE ON CLOSEST HOLE BETWEEN SHOVEL *
C	* PROJECTION INTERCEPT AND TOP OF FORMATION AND  *
C	* BETWEEN SHOVEL PROJECTION INTERCEPT PLUS BANK  *
C	* HEIGHT AND BOTTOM OF HOLE                      *

	FTOP=DHS(5,CD)-ECH
	IF(DHS(5,CD).EQ.0.)FTOP=DHS(4,CD)-ECH
	FBOT=ECHHD-DHS(9,CD)

	GO TO 200

100	CD=CD+1
	IF(CD-N)10,10,110

110	IF(MODE.EQ.'BATCH')THEN
	  WRITE (99,120)SHVL,EWN,EWA,NSN,NSA,ELEV,HD
120	  FORMAT(1X/5X,'CANNOT SATISFY CLOSEST HOLE FOOTAGE INTERCEPT R'
	1,'EQUIREMENTS FOR SHVL ENTRY ',A3,1X,F7.0,A1,1X,F6.0,A1,F6.0,F4
	2.0)
	ELSE
	  PRINT 120,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	END IF

        KERR=1

200	RETURN
	END
