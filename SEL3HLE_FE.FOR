	SUBROUTINE SEL3HLE_FE(FILE_TYPE,JERR,MODE)

C	**************************************************************
C	* THIS SUBROUTINE SELECTS THE 3 CLOSEST HOLES TO THE CURRENT *
C	* SHOVEL LOCATION TO USE IN COMPUTING THE STRIKE AND DIP     *
C	* THE 3 HOLES MUST HAVE AT LEAST 1 LAYER IN COMMON AMONG THE *
C	* (LS,LC3,LC1) LAYERS AND ALL 3 HOLES SELECTED CAN NOT BE ON *
C	* THE SAME SECTION                                           *
C	**************************************************************

	IMPLICIT	NONE
	EXTERNAL	DSC$K_DTYPE_F
	COMMON		/SUBS/STKS,STKD,DIPS,DIPD,DIR,CL
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2	N,CD,CD1,CD2,J,K,L,MD,FD,HE,ME,LE,JERR,CL
	INTEGER*2	KEYBUF(5)/1,0,0,0,4/
	INTEGER*4	FLAGSUB/64/
	REAL*4		XCD,XCD1,XCD2,CLAY,DIS1,DIS2,DIS3,DIS4,TOTDIST,
	1		STKS,STKD,DIPS,DIPD,DIR,SHVL,EWN,EWA,NSN,NSA,
	2		ELEV,HD,ECH,ECHHD
	REAL*4		SLOPE1,SLOPE2,SLOPE3
	CHARACTER	FILE_TYPE*4,FILEIN9*22,FILEIN10*23,DELETE9*31,
	1		DELETE10*32,MODE*5,DUMMY(3)*8
	CHARACTER	BLAY(4)*3/'SUR ','LS  ','LC3 ','LC1 '/

	STRUCTURE /DHSS/

	  CHARACTER*8	DHOLE		! BOT(1)=BOTTOM SURFACE
	  REAL*4	EWN		! BOT(2)=BOTTOM LS
	  REAL*4	NSN		! BOT(3)=BOTTOM LC3
	  REAL*4	COLLAR		! BOT(4)=BOTTOM LC1
	  REAL*4	BOT(5)		! BOT(5)=BOTTOM HOLE
	  REAL*4	DIST

	END STRUCTURE

	RECORD/DHSS/DHS(25)

	CD=0			! ZERO OUT CLOSEST HOLE LOCATOR

	FILEIN9='SUBS:SEL3HLE_'//FILE_TYPE//'.TPE9'
	FILEIN10='SUBS:SEL3HLE_'//FILE_TYPE//'.TPE10'

	OPEN(9,FILE=FILEIN9,STATUS='NEW',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=11)

C	* 1) BEGINNING WITH THE CLOSEST HOLE IN THE "DHS" ARRAY, FIND *
C	*    ALL THE COMBINATIONS OF THAT HOLE WITH THE REMAINING 24  *
C	*    HOLES TAKEN 3 AT A TIME                                  *
C	* 2) DETERMINE THE TOTAL DISTANCE FROM THE SHOVEL OF THE 3    *
C	*    HOLES IN EACH GROUP (DIST 1 + DIST 2 + DIST 3)           *
C	* 3) WRITE THE 3 HOLES IN GROUP PLUS TOTAL DISTANCE FOR GROUP *
C	*    FOR ALL THE 3 HOLE GROUPS IN "DHS" TO TAPE9              *
C	* 4) RETURN TO STEP 1), TAKE THE NEXT CLOSEST HOLE IN THE     *
C	*    "DHS" ARRAY AND REPEAT STEPS (1-3) UNTIL ALL 25 HOLES    *
C	*    HAVE BEEN CYCLED                                         *
C	* 5) SORT TAPE9 BY TOTAL DISTANCE FOR EACH 3 HOLE GROUP AND   *
C	*    WRITE OUTPUT TO TAPE10                                   *
C	* 6) CHECK EACH 3 HOLE GROUP IN CLOSEST TOTAL DISTANCE ORDER  *
C	*    UNTIL A GROUP IS FOUND THAT SATISFIES THE COMMON LAYER   *
C	*    AND SECTION REQUIREMENTS                                 *

100	CD=CD+1			! INCREMENT CLOSEST HOLE COUNTER

C	* CHECK CLOSEST HOLE TO INSURE THAT IT HAS AT LEAST *
C	* 1 OF THE 3 ACCEPTABLE LAYERS (LS,LC3,LC1)         *

	DO 200 K=2,4
	IF(DHS(CD).BOT(K).GT.0.)GO TO 300
200	CONTINUE
	GO TO 100

C	* SET AND INCREMENT CLOSEST HOLE + 1 *
C	* AND CLOSEST HOLE + 2 COUNTERS      *

300	CD1=CD
400	CD1=CD1+1
	CD2=CD1+1 
	DIS1=ABS(DHS(CD).EWN-DHS(CD1).EWN)	! E-W DIS BETWEEN HOLE1 & NEXT
	DIS3=ABS(DHS(CD).NSN-DHS(CD1).NSN)	! N-S DIS BETWEEN HOLE1 & NEXT

C	* EVALUATE HOLES IN GROUPS OF 3 AND WRITE TO TAPE9 *

	DO 1000 J=CD2,N

C	* DETERMINE IF 3 HOLE GROUP IS TOO CLOSE IN E-W *
C	* OR N-S DIRECTION TO COMPUTE PROPER STRIKE/DIP    *

	DIS2=ABS(DHS(CD).EWN-DHS(J).EWN)	! E-W DIS BETWEEN HOLES 1 & J
	IF(DIS1.LE.150..AND.DIS2.LE.150.)GO TO 1000

	DIS4=ABS(DHS(CD).NSN-DHS(J).NSN)	! N-S DIS BETWEEN HOLES 1 & J
	IF(DIS3.LT.100..AND.DIS4.LT.100.)GO TO 1000

C	* IF 3 HOLES FORM A STRAIGHT LINE, THEY CANNOT *
C	* FORM A UNIQUE PLANE AND MUST BE SKIPPED *

	IF((DHS(CD1).EWN-DHS(CD).EWN).EQ.0.)THEN
	  SLOPE1=100000.
	ELSE
	  SLOPE1=(DHS(CD1).NSN-DHS(CD).NSN)/(DHS(CD1).EWN-DHS(CD).EWN)
	END IF

	IF((DHS(J).EWN-DHS(CD1).EWN).EQ.0.)THEN
	  SLOPE2=100000.
	ELSE
	  SLOPE2=(DHS(J).NSN-DHS(CD1).NSN)/(DHS(J).EWN-DHS(CD1).EWN)
	END IF

	IF((DHS(J).EWN-DHS(CD).EWN).EQ.0.)THEN
	  SLOPE3=100000.
	ELSE
	  SLOPE3=(DHS(J).NSN-DHS(CD).NSN)/(DHS(J).EWN-DHS(CD).EWN)
	END IF

	IF(SLOPE1.EQ.SLOPE2.AND.SLOPE2.EQ.SLOPE3)GO TO 1000

C	* DETERMINE IF 3 HOLE GROUP HAS ONE OF THE 3 *
C	* ACCEPTABLE LAYERS (LS,LC3,LC1) IN COMMON   *
C	* BEGINNING WITH LC1 AND ASSIGN COMMOM LAYER *
C	* NUMBER (CLAY) TO GROUP *

	DO 500 L=4,2,-1
	IF(DHS(CD).BOT(L).GT.0..AND.DHS(CD1).BOT(L).GT.0..AND.DHS(J).BOT
	1(L).GT.0.)THEN
	  CLAY=L		! SELECTED 3 HOLE GROUP LAYER LOCATOR
	  GO TO 600
	END IF
500	CONTINUE
	GO TO 1000

600	TOTDIST=DHS(CD).DIST+DHS(CD1).DIST+DHS(J).DIST
	XCD=CD
	XCD1=CD1
	XCD2=J

C	* WRITE 3 HOLE GROUP LOCATORS, HOLE #'S AND TOTAL *
C	* DISTANCE VALUE TO TAPE 9                        *

	WRITE (9)TOTDIST,XCD,XCD1,XCD2,CLAY,DHS(CD).DHOLE,DHS(CD1).DHOLE
	1,DHS(J).DHOLE

1000	CONTINUE

	IF(CD1.LT.(N-1))GO TO 400
	IF(CD.LT.(N-2))GO TO 100

	CLOSE(9)

	KEYBUF(2)=%LOC(DSC$K_DTYPE_F)		! DISTANCE KEY

C	* SORT TAPE9 BY TOTAL DISTANCE AND WRITE OUTPUT TO TAPE10 *

	CALL SOR$PASS_FILES(FILEIN9,FILEIN10)
	CALL SOR$BEGIN_SORT(KEYBUF,,,,,,,)
	CALL SOR$SORT_MERGE()
	CALL SOR$END_SORT()

	OPEN(10,FILE=FILEIN10,STATUS='OLD',FORM='UNFORMATTED',RECORDTYPE
	1='FIXED',RECL=11)

C	* READ SORTED 3 HOLE GROUPS FROM TAPE10 BEGINNING WITH *
C	* CLOSEST DISTANCE GROUP AND ATTEMPT TO COMPUTE STRIKE *
C	* AND DIP FOR SHOVEL LOCATION                          *

1100	READ (10,END=1200)TOTDIST,XCD,XCD1,XCD2,CLAY,DUMMY

C	* ASSIGN CLOSEST,MIDDLE, FAR DISTANCE AND *
C	* LAYER LOCATORS FOR HOLES IN GROUP       *

	CD=NINT(XCD)			! CLOSEST HOLE IN GROUP
	MD=NINT(XCD1)			! MIDDLE HOLE IN GROUP
	FD=NINT(XCD2)			! FARTHEST HOLE IN GROUP      
	CL=NINT(CLAY)			! COMMON LAYER LOCATOR

C	* ASSIGN LOW ELEVATION(LE), MIDDLE ELEVATION(ME), AND *
C	* HIGH ELEVATION(HE) LOCATORS FOR HOLES IN GROUP *

	IF(DHS(FD).BOT(CL).GE.DHS(CD).BOT(CL).AND.DHS(FD).BOT(CL).GE.DHS
	1(MD).BOT(CL))THEN

	  HE=FD				! HIGH ELEV IS ON FARTHEST HOLE

	  IF(DHS(MD).BOT(CL).GE.DHS(CD).BOT(CL))THEN
	    ME=MD
	    LE=CD
	  ELSE
	    ME=CD
	    LE=MD
	  END IF

	ELSE IF(DHS(MD).BOT(CL).GE.DHS(CD).BOT(CL).AND.DHS(MD).BOT(CL).G
	1E.DHS(FD).BOT(CL))THEN

	  HE=MD				! HIGH ELEV IS ON MIDDLE HOLE

	  IF(DHS(FD).BOT(CL).GE.DHS(CD).BOT(CL))THEN
	    ME=FD
	    LE=CD
	  ELSE
	    ME=CD
	    LE=FD
	  END IF

	ELSE

	  HE=CD				! HIGH ELEV IS ON CLOSEST HOLE

	  IF(DHS(FD).BOT(CL).GE.DHS(MD).BOT(CL))THEN
	    ME=FD
	    LE=MD
	  ELSE
	    ME=MD
	    LE=FD
	  END IF

	END IF

	WRITE (88,1110)DHS(HE).DHOLE,DHS(ME).DHOLE,DHS(LE).DHOLE,BLAY(CL
	1)
1110	FORMAT(1X/1X,'HIGH ELEV DH = ',A,2X,'MIDDLE ELEV DH = ',A,2X,
	1'LOW ELEV DH = ',A,5X,'COMMON LAYER = ',A)

C	* CALL "STRKDIP_FE" SUBROUTINE TO COMPUTE *
C	* STRIKE AND DIP FOR CURRENT SHOVEL LOCATION *

	CALL STRKDIP_FE(HE,ME,LE,MODE)

	IF(DIPD-15.)1300,1300,1100	! REPEAT PROCESS IF DIP > 15 DEG

1200	IF(MODE.EQ.'BATCH')THEN
	  WRITE (99,1210)SHVL,EWN,EWA,NSN,NSA,ELEV,HD
1210	  FORMAT(1X/1X,'CANNOT FIND 3 HOLES WITH COMMON LAYER TO COMPUT'
	1,'E STRIKE FOR SHVL ENTRY ',A3,1X,F8.0,A1,F8.0,A1,F6.0,F4.0)
	ELSE
	  PRINT 1210,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	END IF
	JERR=1

C	* CLOSE AND DELETE UNITS 9 AND 10 *

1300	CLOSE(9)
	CLOSE(10)
	DELETE9='DELETE '//FILEIN9//';*'
	CALL LIB$SPAWN(DELETE9,,,FLAGSUB)
	DELETE10='DELETE '//FILEIN10//';*'
	CALL LIB$SPAWN(DELETE10,,,FLAGSUB)

	RETURN
	END
