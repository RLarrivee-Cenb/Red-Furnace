	SUBROUTINE ENGR_TO_STRATALOGGER(ENGBH,SUFFIX,ROW,HOLE,STLBH)

C	**************************************************************
C	* THIS SUBROUTINE CONVERTS THE ENGINEERING BLAST HOLE NUMBER *
C	* TO THE STRATALOGGER NAMING CONVENTION FOR THE BLAST HOLE   *
C	**************************************************************

	IMPLICIT	NONE
	INTEGER*4	ROW,HOLE,ENGBH,HOLES
	CHARACTER	SUFFIX*1,STLBH*3
	CHARACTER*1	ROWN(20)/'A','B','C','D','E','F','G','H','I',
	1		'J','K','L','M','N','O','P','Q','R','S','T'/
	CHARACTER*1	ROWS(7)/' ','Z','Y','X','W','V','U'/

	STLBH='000'

	IF(SUFFIX.EQ.' ')THEN
	  STLBH(1:1)=ROWN(ROW)
	  WRITE (STLBH(2:3),100)HOLE
100	  FORMAT(I2)
	ELSE IF(SUFFIX.EQ.'R')THEN
	  STLBH(1:1)=ROWS(ROW)
	  WRITE (STLBH(2:3),100)HOLE
	ELSE IF(SUFFIX.EQ.'H')THEN
	  STLBH(1:1)=ROWN(ROW)
	  HOLES=101-HOLE
	  WRITE (STLBH(2:3),100)HOLES
	ELSE IF(SUFFIX.EQ.'Z')THEN
	  STLBH(1:1)=ROWS(ROW)
	  HOLES=101-HOLE
	  WRITE (STLBH(2:3),100)HOLES
	END IF

	IF(STLBH(2:2).EQ.' ')STLBH(2:2)='0'

	RETURN
	END
