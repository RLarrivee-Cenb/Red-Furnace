	SUBROUTINE PRINTER(CONTROL)

C	********************************************
C	* THIS SUBROUTINE TURNS PRINTER ON AND OFF *
C	********************************************

	IMPLICIT	NONE
	CHARACTER	ON*3/'[5i'/,OFF*3/'[4i'/,ESC*1/27/
	CHARACTER*4	PRINTER_ON,PRINTER_OFF
	CHARACTER*3	CONTROL

	PRINTER_ON=ESC//ON		! COCANTENATE ESC AND ON
	PRINTER_OFF=ESC//OFF		! COCANTENATE ESC AND OFF

	IF(CONTROL.EQ.'ON ')THEN

C	  * TURN PRINTER ON *

	  PRINT 10,PRINTER_ON
10	  FORMAT(' ',A)

	ELSE IF(CONTROL.EQ.'OFF')THEN

C	  * TURN PRINTER OFF *

	  PRINT 10,PRINTER_OFF

	END IF

	RETURN
	END
