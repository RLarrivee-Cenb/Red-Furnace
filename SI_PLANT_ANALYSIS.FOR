	SUBROUTINE SI_PLANT_ANALYSIS(WRS,WRI,FES,FEI,SLS,SLI,GRD,BWT,MOD
	1E,SKIPEG)

C	**********************************************************
C	* THIS SUBROUTINE COMPUTES DAVIS TUBE,CONCENTRATE AND    *
C	* AGGLOMERATE ANALYSIS USING PETE NILES' SLOPE/INTERCEPT *
C	* AND SIO2/IRON DIFFERENCE FORMULAS PLUS EXISTING PLANT  *
C	* CONVERSION FORMULAS                                    *
C	**********************************************************

	IMPLICIT	NONE
	COMMON		/PETE/DWR,DFE,DSL,CWR,CFE,CSL,AWR,AFE,ASL
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/ERRCOM/MERR
	INTEGER*2	JT,MERR,SKIPEG
	REAL*4		GRD,BWT,EGRD,EG95,MAGF,DS95,DW95,OXDF,KVAL,
	1		KHIGH,KLOW,OHIGH,OLOW,KRGE,ORGE,KDIF,ODIF,
	2		SDIF,FDIF
	REAL*4		DWR,DFE,DSL,CWR,CFE,CSL,AWR,AFE,ASL
	REAL*4		SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	REAL*4		KVT(10)/.0,.1,.25,.4,.5,.6,.7,.8,.9,1.0/
	REAL*4		OFT(10)/1.,.995,.98,.92,.855,.75,.61,.44,.23,.0/
	REAL*8		WRS,WRI,FES,FEI,SLS,SLI
	CHARACTER	MODE*5

C	*********************************************************
C	*			REVISIONS			*
C	*							*
C	* 09/30/1994 - CONVERTED TO OPEN VMS AXP		*
C	*							*
C	* 08/08/1997 - REVISED CODE TO SKIP EQUIVALENT GRIND    *
C	*	       CONVERSION FOR NEW DDH DATA (27500 DHS)  *
C	*********************************************************

C	* ZERO PLANT ANALYSIS *

	DWR=0.
	DFE=0.
	DSL=0.
	CWR=0.
	CFE=0.
	CSL=0.
	AWR=0.
	AFE=0.
	ASL=0.
	MERR=0				! ZERO ERROR FLAG

C	* IF LAYER IS SUR,ROCK, OR OTHER NON-ANALYSIS MATERIAL *
C	* BWT WILL BE 0 AND REMAINDER OF SUBROUTINE IS SKIPPED *
C	* AND CONTROL WILL BE RETURNED TO MAIN PROGRAM *

	IF(BWT.EQ.0.)GO TO 500

C	* COMPUTE OLD SCREEN EQUIVALENT GRIND *
C	* FOR NEW SCREEN PLANT GRIND *

	IF(SKIPEG.EQ.1)THEN
	  EGRD=GRD
	ELSE
	  EGRD=GRD*.84135+17.093
	END IF

C	* COMPUTE NEW S/I DAVIS TUBE ANALYSIS *

	DWR=WRS*EGRD+WRI
	IF(DWR.LT.0.)DWR=0.
	DFE=FES*EGRD+FEI
	IF(DFE.LT.0.)DFE=0.
	DSL=SLS*EGRD+SLI
	IF(DSL.LT.0.)DSL=0.

C	* COMPUTE OLD SCREEN EQUIVALENT GRIND AT 95%-270 *

	IF(SKIPEG.EQ.1)THEN
	  EG95=95.0
	ELSE
	  EG95=95.0*.84135+17.093
	END IF

C	* COMPUTE MAGFE RECOVERY FACTOR (MAGF)

	DS95=SLS*EG95+SLI		! COMPUTE DSL @ EQV GRD 95.0
	MAGF=98.1363-(DS95*1.2545)

C	* COMPUTE OXIDATION FACTOR (OXDF) *

	DW95=WRS*EG95+WRI		! COMPUTE DWR @ EQV GRD 95.0
	IF(DW95.LT.0.)DW95=0.

C	* IF DWR > BASE WEIGHT FACTOR FOR LAYER *
C	* SET QXIDATION FACTOR TO 1 *

	IF(DW95.GE.BWT)THEN
	  OXDF=1
	  GO TO 300
	END IF

	KVAL=(BWT-DW95)/BWT		! COMPUTE "K" VALUE
	IF(KVAL.LT.0.)KVAL=0.

C	* COMPUTE "K" VALUE AND OXIDATION VALUE *
C	* HIGH AND LOW LIMITS *

	DO 100 JT=1,9
	IF(KVAL.GE.KVT(JT).AND.KVAL.LE.KVT(JT+1))THEN
	  KLOW=KVT(JT)	
	  KHIGH=KVT(JT+1)
	  OLOW=OFT(JT)
	  OHIGH=OFT(JT+1)
	  GO TO 200
	END IF
100	CONTINUE

	IF(MODE.EQ.'BATCH')THEN
	  WRITE (99,110)KVAL,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
110	  FORMAT(1X/1X,'COMPUTED K VALUE ',F9.4,' EXCEEDS K TABLE LIMIT'
	1,'S FOR SAMPLE ',A3,2(1X,F6.0,A1),1X,F5.0,1X,F3.0)
	ELSE
	  PRINT 110,KVAL,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	END IF
	MERR=1
	GO TO 500

200	KRGE=KHIGH-KLOW			! RANGE OF "K" TABLE VALUE
	ORGE=OHIGH-OLOW			! RANGE OF OXD TABLE VALUE
	KDIF=KVAL-KLOW			! COMPUTE "K" DIFFERENCE

	IF(KRGE.EQ.0.)THEN
	  ODIF=KDIF*ORGE/(KRGE+.001)	! COMPUTE OXD DIFFERENCE
	ELSE
	  ODIF=KDIF*ORGE/KRGE		! COMPUTE OXD DIFFERENCE
	END IF

	OXDF=OLOW+ODIF

C	* COMPUTE S/I CONCENTRATE ANALYSIS *

300	IF((DFE-2.6).EQ.0.)THEN
	  CWR=DWR*(DFE/((DFE-2.6)+.1))*MAGF/100.*OXDF
	ELSE
	  CWR=DWR*(DFE/(DFE-2.6))*MAGF/100.*OXDF
	END IF

	IF(CWR.LT.0.)CWR=0.

C	* SDIF = SILICA DIFFERENCE CORRECTION FACTOR *
C	* LAST UPDATED  11/29/89 *

	SDIF=(.00193)*(1.07996**GRD)
	CSL=DSL+SDIF
	IF(CSL.LT.0.)CSL=0.

C	* FDIF = IRON DIFFERENCE CORRECTION FACTOR *
C	* LAST UPDATED  4/20/87 *

	FDIF=SDIF*.724
	CFE=DFE-FDIF
	IF(CFE.LT.0.)CFE=0.

C	* COMPUTE S/I AGGLOMERATE ANALYSIS *

	AWR=(.0032*CFE+.8058)*CWR
	AFE=.7857*CFE+13.17
	ASL=CSL+.2

500	RETURN
	END
