	SUBROUTINE WRITE_PITCH(PITCH,TAPE_NO)

C	*************************************************************
C	* THIS SUBROUTINE SETS THE LA120 TO A SPECIFIED PRINT PITCH *
C	* AND IS USED IN CONJUNCTION WITH "WRITE" COMMANDS          *
C	*************************************************************

	IMPLICIT	NONE
	INTEGER*2	TAPE_NO
	REAL*4		PITCH
	CHARACTER	SET*4,ESC*1/27/

	IF(PITCH.EQ.5.)THEN
	  SET=ESC//'[5w'
	ELSE IF(PITCH.EQ.6.)THEN
	  SET=ESC//'[6w'
	ELSE IF(PITCH.EQ.6.6)THEN
	  SET=ESC//'[7w'
	ELSE IF(PITCH.EQ.8.25)THEN
	  SET=ESC//'[8w'
	ELSE IF(PITCH.EQ.10.)THEN
	  SET=ESC//'[1w'
	ELSE IF(PITCH.EQ.12.)THEN
	  SET=ESC//'[2w'
	ELSE IF(PITCH.EQ.13.2)THEN
	  SET=ESC//'[3w'
	ELSE IF(PITCH.EQ.16.5)THEN
	  SET=ESC//'[4w'
	ELSE
	  SET=ESC//'[1w'
	END IF

	WRITE (TAPE_NO,100)SET
100	FORMAT(1X,A)

	RETURN
	END
