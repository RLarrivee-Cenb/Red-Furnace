	SUBROUTINE RESET_PRINTER_OUTPUT_FILE(TAPENUM)

C	***************************************************************
C	* THIS SUBROUTINE READS THE PRINTER OUTPUT WRITE FILE FOR THE *
C	* CALLING PROGRAM UNTIL AN END OF FILE IS REACHED AND THEN    *
C	* RETURNS CONTROL TO THE CALLING PROGRAM SO THAT FURTHER      *
C	* OUTPUT MAY BE WRITTEN TO IT                                 *
C	***************************************************************

	IMPLICIT	NONE
	INTEGER*2	TAPENUM
	CHARACTER*140	TAPEDATA

C	* READ THE NEXT ENTRY FROM TAPE "TAPENUM" *

100	READ (TAPENUM,110,END=200)TAPEDATA
110	FORMAT(A)
	GO TO 100

200	RETURN
	END
