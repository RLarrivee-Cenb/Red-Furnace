	SUBROUTINE COMPUTE_ELAPSED_TIME(START,ELAPSED)

	INTEGER*2	J
	REAL*4		HH,MM,SS,HSS,START,CURRENT,SECS
	CHARACTER	TIME*23,SHH*2,SMM*2,SSS*5,ELAPSED*11

	STRUCTURE	/TIMES/

	INTEGER*4	HH
	INTEGER*4	MM
	REAL*4		SS

	END STRUCTURE

	RECORD/TIMES/COMPUTE

	CALL LIB$DATE_TIME(TIME)

	READ (TIME(13:23),100)HH,MM,SS,HSS
100	FORMAT(3(F2.0,1X),F2.0)

	CURRENT=HH*3600.+MM*60.+SS+HSS*.01
	SECS=CURRENT-START
	COMPUTE.HH=INT(SECS/3600.)
	COMPUTE.MM=INT((SECS-COMPUTE.HH*3600.)/60.)
	COMPUTE.SS=SECS-COMPUTE.HH*3600.-COMPUTE.MM*60.

	WRITE (SHH,200)COMPUTE.HH
200	FORMAT(I2)
	WRITE (SMM,200)COMPUTE.MM
	WRITE (SSS,300)COMPUTE.SS
300	FORMAT(F5.2)

	ELAPSED=SHH//':'//SMM//':'//SSS

	DO J=1,11
	IF(ELAPSED(J:J).EQ.' ')THEN
	  ELAPSED(J:J)='0'
	END IF
	END DO

	RETURN
	END
