	SUBROUTINE SELINTV_FW(RADIUS,MODE)

C	**************************************************************
C	* THIS SUBROUTINE PROJECTS THE SHOVEL ELEVATION ALONG THE    *
C	* STRIKE/DIP TO DETERMINE THE ELEVATION AND FOOTAGE INTERVAL *
C	* AT WHICH THE SHOVEL PROJECTION INTERCEPTS THE "DHS" DRILL  *
C	* HOLES                                                      *
C	**************************************************************

	IMPLICIT	NONE
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	COMMON		/COM3/DHI,M
	INTEGER*2       N,CD,M,JZ,KZ,L,LSM,L3M,L1M
	REAL*4		SHVL,EWN,EWA,NSN,NSA,ELEV,HD,ECH,ECHHD,LSICPT,
	1		L3ICPT,L1ICPT,RADIUS,EIH,EIHHD
	CHARACTER	MODE*5

	STRUCTURE /DHSS/

	  CHARACTER*8	DHOLE		! BOT(1)=BOTTOM SURFACE
	  REAL*4	EWN		! BOT(2)=BOTTOM LS
	  REAL*4	NSN		! BOT(3)=BOTTOM LC3
	  REAL*4	COLLAR		! BOT(4)=BOTTOM LC1
	  REAL*4	BOT(5)		! BOT(5)=BOTTOM HOLE
	  REAL*4	DIST

	END STRUCTURE

	RECORD/DHSS/DHS(25)

	STRUCTURE /DHIS/

	  CHARACTER*8	DHOLE
	  REAL*4	FTTOP		! ELEV @ TOP DH - ELEV @ TOP BH
	  REAL*4	FTBOT		! ELEV @ TOP DH - ELEV @ BOT BH
	  REAL*4	DIST

	END STRUCTURE

	RECORD/DHIS/DHI(20)

	WRITE (88,100)
100	FORMAT(1X/1X,'"SELINTV_FW" REPORT'/1X,19('-')/)

	M=0				! ZERO OUT "DHI" COUNTER

	DO JZ=1,20
	DHI(JZ).DHOLE='00000000'
	DHI(JZ).FTTOP=0.
	DHI(JZ).FTBOT=0.
	DHI(JZ).DIST=0.
	END DO

C	* DETERMINE THE FEET ABOVE OR BELOW THE BOTTOM OF THE LC1, *
C	* LC3 AND LS LAYERS AT WHICH THE SHOVEL PROJECTION INTERCEPTS *
C	* THE CLOSEST DRILL HOLE *

	LSICPT=0.
	L3ICPT=0.
	L1ICPT=0.
	LSM=0
	L3M=0
	L1M=0

	IF(DHS(CD).BOT(2).GT.0.)THEN
	  LSM=1				! CLOSEST HOLE HAS LS
	  LSICPT=ECH-DHS(CD).BOT(2)	! INTERCEPT FT ABOVE LS
	END IF
	IF(DHS(CD).BOT(3).GT.0.)THEN
	  L3M=1				! CLOSEST HOLE HAS LC3
	  L3ICPT=ECH-DHS(CD).BOT(3)	! INTERCEPT FT ABOVE LC3
	END IF
	IF(DHS(CD).BOT(4).GT.0.)THEN
	  L1M=1				! CLOSEST HOLE HAS LC1
	  L1ICPT=ECH-DHS(CD).BOT(4)	! INTERCEPT FT ABOVE LC1
	END IF

	WRITE (88,110)LSICPT,L3ICPT,L1ICPT
110	FORMAT(1X,'LSICPT = ',F6.1,2X,'L3ICPT = ',F6.1,2X,'L1ICPT = ',F6
	1.1)

C	* USE THE FEET ABOVE/BELOW INTERCEPTS FOR LC1,LC3, AND LS IN *
C	* THE CLOSEST DRILL HOLE TO SELECT THE FOOTAGE INTERVALS TO *
C	* USE FOR EACH OF THE DRILL HOLES IN THE "DHS" ARRAY *
C	* FIRST TEST THE DRILL HOLE FOR THE PRESENCE OF LC1 - IF IT *
C	* EXISTS, USE THE "L1ICPT" PARAMETER TO DETERMINE THE FOOTAGE *
C	* INTERVAL *
C	* IF LC1 IS NOT PRESENT, REPEAT PROCESS WITH "L3ICPT" AND IF *
C	* NECESSARY WITH "LSICPT" *

	WRITE (88,120)
120	FORMAT(1X/1X,'DRILL HOLES SAVED IN "DHI" ARRAY'/1X,32('-')/)

	DO 1000 L=1,N

C	* IF DRILL HOLE EXCEEDS RADIUS OF SELECTION, EXIT LOOP *

	IF(DHS(L).DIST.GT.RADIUS)THEN
	  WRITE (88,200)L,DHS(L).DHOLE,DHS(L).DIST
200	  FORMAT(1X,'EXCEEDED SEARCH RADIUS WITH THE # ',I2,' HOLE IN "'
	1,'DHS" ARRAY - DRILL HOLE ',A8,1X,'AT DISTANCE FROM SHOVEL ',F6
	2.1)
	  GO TO 2000
	END IF

	IF(DHS(L).BOT(4))400,400,300	! CHECK DRILL HOLE FOR LC1

C	* DRILL HOLE HAS LC1 - IF LC1 INTERCEPT FROM CLOSEST *
C	* HOLE > 0, COMPUTE THIS DRILL HOLE INTERVAL USING LC1 *
C	* DRILL HOLE INTERCEPT WILL BE ELEVATION AT BOTTOM OF *
C	* LC1 PLUS FEET ABOVE/BELOW LC1 CLOSEST HOLE INTERCEPT *

300	IF(L1M)400,400,310		! DOES CLOSEST HOLE HAVE LC1 ?
310	EIH=DHS(L).BOT(4)+L1ICPT
	GO TO 900

400	IF(DHS(L).BOT(3))600,600,410	! CHECK DRILL HOLE FOR LC3

C	* DRILL HOLE HAS LC3 - IF LC3 INTERCEPT FROM CLOSEST *
C	* HOLE > 0, COMPUTE THIS DRILL HOLE INTERVAL USING LC3 *
C	* DRILL HOLE INTERCEPT WILL BE ELEVATION AT BOTTOM OF *
C	* LC3 PLUS FEET ABOVE/BELOW LC3 CLOSEST HOLE INTERCEPT *

410	IF(L3M)600,600,500	    	! DOES CLOSEST HOLE HAVE LC3 ?
500	EIH=DHS(L).BOT(3)+L3ICPT
	GO TO 900

600	IF(DHS(L).BOT(2))1000,1000,700	! CHECK DRILL HOLE FOR LS

C	* DRILL HOLE HAS LS - IF LS INTERCEPT FROM CLOSEST *
C	* HOLE > 0, COMPUTE THIS DRILL HOLE INTERVAL USING LS *
C	* DRILL HOLE INTERCEPT WILL BE ELEVATION AT BOTTOM OF *
C	* LS PLUS FEET ABOVE/BELOW LS CLOSEST HOLE INTERCEPT *

700	IF(LSM)1000,1000,800		! DOES CLOSEST HOLE HAVE LS ?
800	EIH=DHS(L).BOT(2)+LSICPT

900	EIH=AINT(EIH+.5)		! TRUNCATE ELEVATION INTERCEPT

C	* IF ELEVATION AT DRILL HOLE INTERCEPT IS GREATER THAN *
C	* ITS COLLAR ELEVATION, SKIP THIS HOLE AND GO TO NEXT *
C	* DRILL HOLE IN "DHS" ARRAY *

	IF(EIH.GT.DHS(L).COLLAR)THEN
	  WRITE (88,910)EIH,DHS(L).DHOLE,DHS(L).COLLAR
910	  FORMAT(1X,'THE DH INTERCEPT ELEVATION (',F6.1,') ON DH ',A8,
	1' EXCEEDS THE DH COLLAR ELEVATION ',F5.0,' - SKIP THIS HOLE')
	  GO TO 1000
	END IF

C	* COMPUTE FOOTAGE ELEVATION AT TOP OF BANK ON DRILL HOLE *

	EIHHD=EIH+HD

C	* IF ELEVATION AT DRILL HOLE INTERCEPT PLUS HOLE DEPTH IS *
C	* LESS THAN ITS BOTTOM OF HOLE ELEVATION, SKIP THIS HOLE AND *
C	* GO TO NEXT DRILL HOLE IN "DHS" ARRAY *

	IF(EIHHD.LE.DHS(L).BOT(5))THEN
	  WRITE (88,920)EIHHD,DHS(L).DHOLE,DHS(L).BOT(5)
920	  FORMAT(1X,'THE DH INTERCEPT ELEVATION + HOLE DEPTH (',F6.1,')'
	1,' ON DH ',A8,' IS < THE DH BOTTOM OF HOLE ELEVATION ',F5.0,
	2' - SKIP THIS HOLE')
	  GO TO 1000
	END IF

C	* LET DRILL HOLE "FOOTAGE FROM" BE COLLAR ELEVATION - *
C	* ELEVATION AT DRILL HOLE INTERCEPT PLUS HOLE DEPTH *
C	* LET DRILL HOLE "FOOTAGE TO" BE COLLAR ELEVATION - *
C	* ELEVATION AT DRILL HOLE INTERCEPT *
C	* MOVE DRILL HOLE#, FOOTAGE FROM, FOOTAGE TO, AND *
C	* DISTANCE FROM SHOVEL TO DRILL HOLE INTO "DHI" ARRAY *
 
	M=M+1
	DHI(M).DHOLE=DHS(L).DHOLE		! DRILL HOLE #
	DHI(M).FTTOP=DHS(L).COLLAR-EIHHD	! FOOTAGE FROM
	IF(DHI(M).FTTOP.LT.0.)DHI(M).FTTOP=0.
	DHI(M).FTBOT=DHS(L).COLLAR-EIH		! FOOTAGE TO
	IF(DHI(M).FTBOT.LT.0.)DHI(M).FTBOT=0.
	DHI(M).DIST=DHS(L).DIST			! DISTANCE FROM SHOVEL TO HOLE

	IF(DHS(L).DIST.EQ.0.)DHI(M).DIST=1.	! INSURE THAT DIST TO SHV > 0

	WRITE (88,930)M,DHI(M).DHOLE,DHI(M).FTTOP,DHI(M).FTBOT,DHI(M).DI
	1ST
930	FORMAT(1X,'M = ',I2,2X,'DHOLE = ',A8,2X,'FOOTAGE FROM = ',F6.1,2
	1X,'FOOTAGE TO = ',F6.1,2X,'DISTANCE FROM SHOVEL = ',F6.1)

C	* SAVE ONLY THE 20 CLOSEST HOLES TO COMPUTE SHOVEL ANALYSIS *

	IF(M.EQ.20)GO TO 2000

1000	CONTINUE

2000	RETURN
	END
