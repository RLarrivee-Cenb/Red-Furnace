	SUBROUTINE COMPUTE_BEARING(EWNA,NSNA,EWNB,NSNB,BEAR)

C	**************************************************************
C	* THIS SUBROUTINE COMPUTES THE BEARING OF A LINE (AB) TURNED *
C	* RIGHT FROM DUE NORTH AND MEASURED IN DEGREES               *
C	**************************************************************

	IMPLICIT	NONE
	REAL*8		EWNA,NSNA,EWNB,NSNB,DELTAX,DELTAY,BEAR

	BEAR=0.D0

	DELTAX=ABS(EWNB-EWNA)
	DELTAY=ABS(NSNB-NSNA)

	IF(NSNB.GT.NSNA.AND.EWNB.GT.EWNA)THEN

C	  * CASE1 -  POINT B IS NORTH AND EAST OF POINT A *

	  BEAR=ATAND(DELTAX/DELTAY)

	ELSE IF(NSNB.LT.NSNA.AND.EWNB.GT.EWNA)THEN

C	  * CASE2 -  POINT B IS SOUTH AND EAST OF POINT A *

	  BEAR=90.D0+ATAND(DELTAY/DELTAX)

	ELSE IF(NSNB.LT.NSNA.AND.EWNB.LT.EWNA)THEN

C	  * CASE3 -  POINT B IS SOUTH AND WEST OF POINT A *

	  BEAR=270.D0-ATAND(DELTAY/DELTAX)

	ELSE IF(NSNB.GT.NSNA.AND.EWNB.LT.EWNA)THEN

C	  * CASE4 -  POINT B IS NORTH AND WEST OF POINT A *

	  BEAR=360.D0-ATAND(DELTAX/DELTAY)

	ELSE IF(NSNB.GT.NSNA.AND.EWNB.EQ.EWNA)THEN

C	  * CASE5 -  POINT B IS STRAIGHT NORTH OF POINT A *

	  BEAR=0.D0

	ELSE IF(NSNB.EQ.NSNA.AND.EWNB.GT.EWNA)THEN

C	  * CASE6 -  POINT B IS STRAIGHT EAST OF POINT A *

	  BEAR=90.D0

	ELSE IF(NSNB.LT.NSNA.AND.EWNB.EQ.EWNA)THEN

C	  * CASE7 -  POINT B IS STRAIGHT SOUTH OF POINT A *

	  BEAR=180.D0

	ELSE IF(NSNB.EQ.NSNA.AND.EWNB.LT.EWNA)THEN

C	  * CASE8 -  POINT B IS STRAIGHT WEST OF POINT A *

	  BEAR=270.D0

	END IF

	RETURN
	END
