	SUBROUTINE COORSUB_FW(CF,FILE_TYPE,IERR,MODE)

C	******************************************************
C	* THIS SUBROUTINE FINDS ALL THE DRILL HOLES ON THE   *
C	* MASTER FAR WEST AND HIBTAC COORDINATE FILES WITHIN *
C	* 1800' OF THE CURRENT SHOVEL LOCATION, WRITES THEM  *
C	* TO TAPE7, SORTS TAPE7 BY DISTANCE FROM THE SHOVEL  *
C	* AND WRITES THE SORT OUTPUT TO TAPE8                *
C	******************************************************

	IMPLICIT	NONE
	EXTERNAL	DSC$K_DTYPE_F
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2	NH,IERR,N,JZ,CF(2),CD,J
	INTEGER*2	KEYBUF(5)/1,0,0,0,4/
	INTEGER*4	RECN,FLAGSUB/64/
	REAL*4		SHVL,EWN,EWA,NSN,NSA,ELEV,HD,EWD,NSD,DIST,ECH,
	1		ECHHD
	CHARACTER	FILE_TYPE*4,FILEIN7*22,FILEIN8*22,DELETE*31,
	1		MODE*5,DHN*8

	STRUCTURE /DHSS/

	  CHARACTER*8	DHOLE		! BOT(1)=BOTTOM SURFACE
	  REAL*4	EWN		! BOT(2)=BOTTOM LS
	  REAL*4	NSN		! BOT(3)=BOTTOM LC3
	  REAL*4	COLLAR		! BOT(4)=BOTTOM LC1
	  REAL*4	BOT(5)		! BOT(5)=BOTTOM HOLE
	  REAL*4	DIST

	END STRUCTURE

	RECORD/DHSS/DHS(25)

	STRUCTURE /CFLES/

	  CHARACTER*8	DHOLE
	  INTEGER*4	NSN
	  CHARACTER*1	NSA
	  INTEGER*4	EWN
	  CHARACTER*1	EWA
	  INTEGER*4	BOTLC3
	  INTEGER*4	BOTLC1
	  INTEGER*4	BOTLS
	  INTEGER*4	FTSUR
	  INTEGER*4	BOTHLE
	  INTEGER*4	COLLAR

	END STRUCTURE

	RECORD/CFLES/CFLE

	NH=0			! #HOLES < 1800' COUNTER
	N=0			! "DHS" COUNTER

	FILEIN7='SUBS:COORSUB_'//FILE_TYPE//'.TPE7'
	FILEIN8='SUBS:COORSUB_'//FILE_TYPE//'.TPE8'

	OPEN(7,FILE=FILEIN7,STATUS='NEW',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=13)

	DO 500 J=1,2

	IF(CF(J).EQ.0)GO TO 500

	RECN=0			! ZERO RECORD COUNTER FOR COORFILE

	WRITE (88,50)J,CF(J)
50	FORMAT(1X/1X,'COORSUB_FW',2X,'J=',I1,1X,'CF(J)=',I2)

C	* READ NEXT ENTRY FROM MASTER COORDINATE FILE *

100	RECN=RECN+1
	READ (CF(J),REC=RECN,ERR=500)CFLE.DHOLE,CFLE.NSN,CFLE.NSA,CFLE.E
	1WN,CFLE.EWA,CFLE.BOTLC3,CFLE.BOTLC1,CFLE.BOTLS,CFLE.FTSUR,CFLE.
	2BOTHLE,CFLE.COLLAR
	IF(CFLE.DHOLE.EQ.'99999999')GO TO 500

C	* INSURE THAT W AND S COORFILE NUMERICS ARE NEGATIVE *

	IF(CFLE.EWA.EQ.'W'.AND.CFLE.EWN.GT.0)CFLE.EWN=-CFLE.EWN
	IF(CFLE.NSA.EQ.'S'.AND.CFLE.NSN.GT.0)CFLE.NSN=-CFLE.NSN

C	* COMPUTE STRAIGHT LINE DISTANCE FROM DRILL HOLE TO SHOVEL *

	EWD=EWN-CFLE.EWN		! E-W DISTANCE FROM D.H. TO SHV
	NSD=NSN-CFLE.NSN		! N-S DISTANCE FROM D.H. TO SHV
	DIST=SQRT(EWD**2+NSD**2)	! DISTANCE FROM D.H. TO SHOVEL

	IF(DIST.LE.1800.)THEN

C	  * WRITE COORFILE DATA TO TAPE7 *

	  WRITE (7)DIST,CFLE.DHOLE,CFLE.NSN,CFLE.NSA,CFLE.EWN,CFLE.EWA,C
	1FLE.BOTLC3,CFLE.BOTLC1,CFLE.BOTLS,CFLE.FTSUR,CFLE.BOTHLE,CFLE.C
	2OLLAR
	  NH=NH+1

	END IF

	GO TO 100

500	CONTINUE

C	* IF NUMBER OF DRILL HOLES WITHIN 1800' OF SHOVEL IS < 3 *
C	* PRINT ERROR MESSAGE AND GO TO NEXT SHOVEL ENTRY        *

	IF(NH.LT.3)THEN
	  IF(MODE.EQ.'BATCH')THEN
	    WRITE (99,600)SHVL,EWN,EWA,NSN,NSA,ELEV,HD
600	    FORMAT(1X/5X,'SHOVEL ENTRY ',A3,F7.0,A1,F6.0,A1,F6.0,F4.0,
	1' HAS LESS THAN 3 HOLES WITHIN 1800'' OF ITS LOCATION')
	  ELSE
	    PRINT 600,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	  END IF
	  IERR=1
	  GO TO 1000
	END IF

	CLOSE(7)

	KEYBUF(2)=%LOC(DSC$K_DTYPE_F)		! DISTANCE KEY

C	* SORT SELECTED COORFILE DATA FROM TAPE7 BY DISTANCE *
C	* AND WRITE OUTPUT TO TAPE8                          *

	CALL SOR$PASS_FILES(FILEIN7,FILEIN8)
	CALL SOR$BEGIN_SORT(KEYBUF,,,,,,,)
	CALL SOR$SORT_MERGE()
	CALL SOR$END_SORT()

	OPEN(8,FILE=FILEIN8,STATUS='OLD',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=13)

	DO JZ=1,25
	DHS(JZ).DHOLE='00000000'
	DHS(JZ).EWN=0.
	DHS(JZ).NSN=0.
	DHS(JZ).COLLAR=0.
	DHS(JZ).BOT(1)=0.
	DHS(JZ).BOT(2)=0.
	DHS(JZ).BOT(3)=0.
	DHS(JZ).BOT(4)=0.
	DHS(JZ).BOT(5)=0.
	DHS(JZ).DIST=0.
	END DO

C	* READ THE SORTED COORFILE DATA FROM  *
C	* TAPE8 AND MOVE THE 25 CLOSEST HOLES *
C	* INTO THE "DHS" STRUCTURE *

700	READ (8,END=1000)DIST,CFLE.DHOLE,CFLE.NSN,CFLE.NSA,CFLE.EWN,CFLE
	1.EWA,CFLE.BOTLC3,CFLE.BOTLC1,CFLE.BOTLS,CFLE.FTSUR,CFLE.BOTHLE,
	2CFLE.COLLAR

	N=N+1
	DHS(N).DHOLE=CFLE.DHOLE		      	! DRILL HOLE #
	DHS(N).EWN=CFLE.EWN		      	! E-W NUMERIC
	DHS(N).NSN=CFLE.NSN		      	! N-S NUMERIC
	DHS(N).COLLAR=CFLE.COLLAR	      	! COLLAR ELEVATION
	DHS(N).BOT(1)=CFLE.COLLAR-CFLE.FTSUR	! BOTTOM SURFACE ELEVATION
	DHS(N).BOT(2)=CFLE.BOTLS	      	! BOTTOM LS ELEVATION
	DHS(N).BOT(3)=CFLE.BOTLC3	      	! BOTTOM LC3 ELEVATION
	DHS(N).BOT(4)=CFLE.BOTLC1	      	! BOTTOM LC1 ELEVATION
	DHS(N).BOT(5)=CFLE.BOTHLE	      	! BOTTOM HOLE ELEVATION
	DHS(N).DIST=DIST		      	! DISTANCE FROM SHOVEL

	IF(N.LT.25)GO TO 700

1000	WRITE (88,1010)
1010	FORMAT(1X,'DRILL HOLES SELECTED BY COORSUB_FW FOR "DHS" '
	1,'ARRAY'/1X,50('-')/)

	DO J=1,N
	WRITE (88,1020)DHS(J).DHOLE,DHS(J).EWN,DHS(J).NSN,DHS(J).COLLAR,
	1DHS(J).BOT(1),DHS(J).BOT(2),DHS(J).BOT(3),DHS(J).BOT(4),DHS(J).
	2BOT(5),DHS(J).DIST
1020	FORMAT(1X,'DHOLE=',A8,1X,'EWN=',F7.0,1X,'NSN=',F7.0,1X,'COLLAR='
	1,F5.0,1X,'BOTSUR=',F5.0,1X,'BOTLS=',F5.0,1X,'BOTLC3=',F5.0,1X,
	2'BOTLC1=',F5.0,1X,'BOTHLE=',F5.0,1X,'DIST=',F7.1)
	END DO

C	* CLOSE AND DELETE UNITS 7 AND 8 *

	CLOSE(7)
	CLOSE(8)

	DELETE='DELETE '//FILEIN7//';*'
	CALL LIB$SPAWN(DELETE,,,FLAGSUB)
	DELETE='DELETE '//FILEIN8//';*'
	CALL LIB$SPAWN(DELETE,,,FLAGSUB)

1100	RETURN
	END
