	SUBROUTINE COORSUB(CF,FILE_TYPE,IERR,MODE)

C	******************************************************
C	* THIS SUBROUTINE FINDS ALL THE DRILL HOLES ON THE   *
C	* MASTER COORDINATE FILE WITHIN 1200' OF THE CURRENT *
C	* SHOVEL LOCATION, WRITES THEM TO TAPE7 AND THEN     *
C	* SORTS TAPE7 BY DISTANCE FROM SHOVEL WRITING SORT   *
C	* OUTPUT TO TAPE8                                    *
C	******************************************************

	IMPLICIT	NONE
	EXTERNAL	DSC$K_DTYPE_F
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2	NH,IERR,N,JZ,KZ,CF,CD
	INTEGER*2	KEYBUF(5)/1,0,0,44,4/
	INTEGER*4	RECN,FLAGSUB/64/
	REAL*4		SHVL,EWN,EWA,NSN,NSA,ELEV,HD,EWD,NSD,DIST,
	1		CFLE(11),DHS(10,25),ECH,ECHHD
	CHARACTER	FILE_TYPE*4,FILEIN7*22,FILEIN8*22,DELETE*31,
	1		MODE*5

C	****************************************
C	* CONVERTED TO OPEN VMS AXP ON 9/29/94 *
C	****************************************

	NH=0			! ZERO OUT HOLE < 1200' COUNTER
	N=0			! ZERO OUT "DHS" COUNTER
	RECN=0			! ZERO RECORD COUNTER FOR COORFILE

	FILEIN7='SUBS:COORSUB_'//FILE_TYPE//'.TPE7'
	FILEIN8='SUBS:COORSUB_'//FILE_TYPE//'.TPE8'

	OPEN(7,FILE=FILEIN7,STATUS='NEW',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=12)
        
C	* READ NEXT ENTRY FROM MASTER COORDINATE FILE *

10	RECN=RECN+1
	READ (CF,REC=RECN,ERR=100)CFLE
	IF(CFLE(1).EQ.99999999.)GO TO 100

C	* INSURE THAT W AND S COORFILE NUMERICS ARE NEGATIVE *

	IF(CFLE(5).EQ.1HW.AND.CFLE(4).GT.0.)CFLE(4)=-CFLE(4)
	IF(CFLE(3).EQ.1HS.AND.CFLE(2).GT.0.)CFLE(2)=-CFLE(2)

C	* COMPUTE STRAIGHT LINE DISTANCE FROM DRILL HOLE TO SHOVEL *

	EWD=EWN-CFLE(4)			! E-W DISTANCE FROM D.H. TO SHV
	NSD=NSN-CFLE(2)			! N-S DISTANCE FROM D.H. TO SHV
	DIST=SQRT(EWD**2+NSD**2)	! DISTANCE FROM D.H. TO SHOVEL
	IF(DIST.GT.1200.)GO TO 10	! SKIP HOLES > 1200' FROM SHOVEL

	WRITE (7)CFLE,DIST		! WRITE COORFILE DATA TO TAPE7
	NH=NH+1

	GO TO 10

C	* IF NUMBER OF DRILL HOLES WITHIN 1200' OF SHOVEL IS < 3 *
C	* PRINT ERROR MESSAGE AND GO TO NEXT SHOVEL ENTRY        *

100	IF(NH.LT.3)THEN
	  IF(MODE.EQ.'BATCH')THEN
	    WRITE (99,110)SHVL,EWN,EWA,NSN,NSA,ELEV,HD
110	    FORMAT(1X/5X,'SHOVEL ENTRY ',A3,F7.0,A1,F6.0,A1,F6.0,F4.0,
	1' HAS LESS THAN 3 HOLES WITHIN 1200'' OF ITS LOCATION')
	  ELSE
	    PRINT 110,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	  END IF
	  IERR=1
	  GO TO 400
	END IF

	CLOSE(7)

	KEYBUF(2)=%LOC(DSC$K_DTYPE_F)		! DISTANCE KEY

C	* SORT SELECTED COORFILE DATA FROM TAPE7 BY DISTANCE *
C	* AND WRITE OUTPUT TO TAPE8                          *

	CALL SOR$PASS_FILES(FILEIN7,FILEIN8)
	CALL SOR$BEGIN_SORT(KEYBUF,,,,,,,)
	CALL SOR$SORT_MERGE()
	CALL SOR$END_SORT()

	OPEN(8,FILE=FILEIN8,STATUS='OLD',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=12)

	DO 200 JZ=1,10
	DO 200 KZ=1,25
200	DHS(JZ,KZ)=0.			! ZERO OUT "DHS" ARRAY

C	* READ THE SORTED COORFILE DATA FROM  *
C	* TAPE8 AND MOVE THE 25 CLOSEST HOLES *
C	* INTO THE "DHS" ARRAY                *

300	READ (8,END=400)CFLE,DIST

	N=N+1
	DHS(1,N)=CFLE(1)		! DRILL HOLE #
	DHS(2,N)=CFLE(4)		! E-W NUMERIC
	DHS(3,N)=CFLE(2)		! N-S NUMERIC
	DHS(4,N)=CFLE(11)		! COLLAR ELEVATION
	DHS(5,N)=CFLE(11)-CFLE(9)	! BOTTOM SURFACE ELEVATION
	DHS(6,N)=CFLE(8)		! BOTTOM LS ELEVATION
	DHS(7,N)=CFLE(6)		! BOTTOM LC3 ELEVATION
	DHS(8,N)=CFLE(7)		! BOTTOM LC1 ELEVATION
	DHS(9,N)=CFLE(10)		! BOTTOM HOLE ELEVATION
	DHS(10,N)=DIST			! DISTANCE FROM SHOVEL

	IF(N.LT.25)GO TO 300

C	* CLOSE AND DELETE UNITS 7 AND 8 *

400	CLOSE(7)
	CLOSE(8)
	DELETE='DELETE '//FILEIN7//';*'
	CALL LIB$SPAWN(DELETE,,,FLAGSUB)
	DELETE='DELETE '//FILEIN8//';*'
	CALL LIB$SPAWN(DELETE,,,FLAGSUB)

500	RETURN
	END
