	SUBROUTINE COORSUB_HIBTAC(CF,FILE_TYPE,IERR,MODE)

C	******************************************************
C	* THIS SUBROUTINE FINDS ALL THE DRILL HOLES ON THE   *
C	* MASTER COORDINATE FILE WITHIN 1800' OF THE CURRENT *
C	* SHOVEL LOCATION, WRITES THEM TO TAPE7 AND THEN     *
C	* SORTS TAPE7 BY DISTANCE FROM SHOVEL WRITING SORT   *
C	* OUTPUT TO TAPE8                                    *
C	******************************************************

	IMPLICIT	NONE
	EXTERNAL	DSC$K_DTYPE_F
	COMMON		/SAMPLE/SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	COMMON		/COM2/DHS,N,CD,ECH,ECHHD
	INTEGER*2	NH,IERR,N,JZ,KZ,CF,CD,J1
	INTEGER*2	KEYBUF(5)/1,0,0,0,4/
	INTEGER*4	RECN,FLAGSUB/64/
	REAL*4		SHVL,EWN,EWA,NSN,NSA,ELEV,HD,EWD,NSD,DIST,
	1		DHS(10,25),ECH,ECHHD
	CHARACTER	FILE_TYPE*4,FILEIN7*24,FILEIN8*24,DELETE*33,
	1		MODE*5

C	*********************************************************
C	*			REVISIONS			*
C	*							*
C	* 05/17/2002 - REVISED THE PROGRAM TO MATCH THE CURRENT	*
C	*	       STRUCTURE OF THE MASTER HIBTAC COORFILE	*
C	*********************************************************

	STRUCTURE	/CFLES/

	  CHARACTER*8	DHOLE
	  INTEGER*4	NSN
	  CHARACTER*1	NSA
	  INTEGER*4	EWN
	  CHARACTER*1	EWA
	  INTEGER*4	BOTLC3
	  INTEGER*4	BOTLC1
	  INTEGER*4	BOTLS
	  INTEGER*4	FTSUR
	  INTEGER*4	BOTHLE
	  INTEGER*4	COLLAR

	END STRUCTURE

	RECORD/CFLES/CFLE

	NH=0			! ZERO OUT HOLE < 1800' COUNTER
	N=0			! ZERO OUT "DHS" COUNTER
	RECN=0			! ZERO RECORD COUNTER FOR COORFILE

	FILEIN7='HIBTAC:COORSUB_'//FILE_TYPE//'.TPE7'
	FILEIN8='HIBTAC:COORSUB_'//FILE_TYPE//'.TPE8'

	WRITE (88,1)FILEIN7,FILEIN8
1	FORMAT(1X,'FILEIN7 = ',A,3X,'FINEIN8 = ',A)

	OPEN(7,FILE=FILEIN7,STATUS='NEW',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=13)
        
C	* READ NEXT ENTRY FROM MASTER COORDINATE FILE *

10	RECN=RECN+1
	READ (CF,REC=RECN,ERR=100)CFLE.DHOLE,CFLE.NSN,CFLE.NSA,CFLE.EWN,
	1CFLE.EWA,CFLE.BOTLC3,CFLE.BOTLC1,CFLE.BOTLS,CFLE.FTSUR,CFLE.BOT
	2HLE,CFLE.COLLAR

C	* INSURE THAT W AND S COORFILE NUMERICS ARE NEGATIVE *

	IF(CFLE.EWA.EQ.'W'.AND.CFLE.EWN.GT.0.)CFLE.EWN=-CFLE.EWN
	IF(CFLE.NSA.EQ.'S'.AND.CFLE.NSN.GT.0.)CFLE.NSN=-CFLE.NSN

C	* COMPUTE STRAIGHT LINE DISTANCE FROM DRILL HOLE TO SHOVEL *

	EWD=EWN-CFLE.EWN		! E-W DISTANCE FROM D.H. TO SHV
	NSD=NSN-CFLE.NSN		! N-S DISTANCE FROM D.H. TO SHV
	DIST=SQRT(EWD**2+NSD**2)	! DISTANCE FROM D.H. TO SHOVEL
	IF(DIST.GT.1800.)GO TO 10	! SKIP HOLES > 1800' FROM SHOVEL

C	* WRITE COORFILE DATA TO TAPE7 *

	WRITE (7)DIST,CFLE.DHOLE,CFLE.NSN,CFLE.NSA,CFLE.EWN,CFLE.EWA,CFL
	1E.BOTLC3,CFLE.BOTLC1,CFLE.BOTLS,CFLE.FTSUR,CFLE.BOTHLE,CFLE.COL
	2LAR
	NH=NH+1

	GO TO 10

C	* IF NUMBER OF DRILL HOLES WITHIN 1800' OF SHOVEL IS < 3 *
C	* PRINT ERROR MESSAGE AND GO TO NEXT SHOVEL ENTRY        *

100	IF(NH.LT.3)THEN
	  IF(MODE.EQ.'BATCH')THEN
	    WRITE (99,110)SHVL,EWN,EWA,NSN,NSA,ELEV,HD
110	    FORMAT(1X/5X,'SHOVEL ENTRY ',A3,F7.0,A1,F6.0,A1,F6.0,F4.0,
	1' HAS LESS THAN 3 HOLES WITHIN 1800'' OF ITS LOCATION')
	  ELSE
	    PRINT 110,SHVL,EWN,EWA,NSN,NSA,ELEV,HD
	  END IF
	  IERR=1
	  GO TO 400
	END IF

	CLOSE(7)

	KEYBUF(2)=%LOC(DSC$K_DTYPE_F)		! DISTANCE KEY

C	* SORT SELECTED COORFILE DATA FROM TAPE7 BY DISTANCE *
C	* AND WRITE OUTPUT TO TAPE8                          *

	CALL SOR$PASS_FILES(FILEIN7,FILEIN8)
	CALL SOR$BEGIN_SORT(KEYBUF,,,,,,,)
	CALL SOR$SORT_MERGE()
	CALL SOR$END_SORT()

	OPEN(8,FILE=FILEIN8,STATUS='OLD',FORM='UNFORMATTED',RECORDTYPE=
	1'FIXED',RECL=13)

	DO 200 JZ=1,10
	DO 200 KZ=1,25
200	DHS(JZ,KZ)=0.			! ZERO OUT "DHS" ARRAY

C	* READ THE SORTED COORFILE DATA FROM  *
C	* TAPE8 AND MOVE THE 25 CLOSEST HOLES *
C	* INTO THE "DHS" ARRAY                *

300	READ (8,END=400)DIST,CFLE.DHOLE,CFLE.NSN,CFLE.NSA,CFLE.EWN,CFLE.
	1EWA,CFLE.BOTLC3,CFLE.BOTLC1,CFLE.BOTLS,CFLE.FTSUR,CFLE.BOTHLE,C
	2FLE.COLLAR

	N=N+1
	READ (CFLE.DHOLE(5:8),310)DHS(1,N)	! DRILL HOLE
310	FORMAT(F4.0)
	DHS(2,N)=CFLE.EWN			! E-W NUMERIC
	DHS(3,N)=CFLE.NSN			! N-S NUMERIC
	DHS(4,N)=CFLE.COLLAR			! COLLAR ELEVATION
	DHS(5,N)=CFLE.COLLAR-CFLE.FTSUR		! BOTTOM SURFACE ELEVATION
	DHS(6,N)=CFLE.BOTLS			! BOTTOM LS ELEVATION
	DHS(7,N)=CFLE.BOTLC3			! BOTTOM LC3 ELEVATION
	DHS(8,N)=CFLE.BOTLC1			! BOTTOM LC1 ELEVATION
	DHS(9,N)=CFLE.BOTHLE			! BOTTOM HOLE ELEVATION
	DHS(10,N)=DIST				! DISTANCE FROM SHOVEL

	WRITE (88,391)N,(DHS(J1,N),J1=1,10)
391	FORMAT(1X,'COORSUB_HIBTAC  -  DHS(1-10,',I2,') = ',F9.0,1X,F8.0,
	11X,F8.0,6(1X,F5.0),1X,F6.0)

	IF(N.LT.25)GO TO 300

C	* CLOSE AND DELETE UNITS 7 AND 8 *

400	CLOSE(7)
	CLOSE(8)
	DELETE='DELETE '//FILEIN7//';*'
	WRITE (88,401)DELETE
401	FORMAT(1X,'DELETE = ',A)
	CALL LIB$SPAWN(DELETE,,,FLAGSUB)
	DELETE='DELETE '//FILEIN8//';*'
	WRITE (88,401)DELETE
	CALL LIB$SPAWN(DELETE,,,FLAGSUB)

	WRITE (88,410)
410	FORMAT(1X)

500	RETURN
	END
