	SUBROUTINE SPLIT_DATE(STATUS,SPLITS)

C	*****************************************************
C	* THIS SUBROUTINE TAKES A DATE IN XX/XX/XX FORMAT   *
C	* AND SEPERATES IT INTO INTEGER MONTH, DAY AND YEAR *
C	* VALUES                                            *
C	*****************************************************

	IMPLICIT	NONE
	INTEGER*2	SPLITS(3),LOOP,FIRST,LAST,LENGTH
	CHARACTER	STATUS*8

	FIRST=1

C	* COMPUTE INTEGER MONTH *

	DO 200 LOOP=FIRST,8

	IF(STATUS(LOOP:LOOP).EQ.'/')THEN	! FIND / AFTER MONTH

	  LAST=LOOP-1				! LAST DIGIT IN MONTH
	  LENGTH=LAST-FIRST+1			! LENGTH OF MONTH FIELD

	  IF(LENGTH.EQ.1)THEN
	    READ (STATUS(FIRST:LAST),110)SPLITS(1)
110	    FORMAT(I1)
	  ELSE
	    READ (STATUS(FIRST:LAST),120)SPLITS(1)
120	    FORMAT(I2)
	  END IF

	  FIRST=LOOP+1				! FIRST DIGIT IN DAY
	  GO TO 210

	END IF

200	CONTINUE

C	* COMPUTE INTEGER DAY *

210	DO 300 LOOP=FIRST,8

	IF(STATUS(LOOP:LOOP).EQ.'/')THEN	! FIND / AFTER DAY

	  LAST=LOOP-1				! LAST DIGIT IN DAY
	  LENGTH=LAST-FIRST+1			! LENGTH OF DAY FIELD

	  IF(LENGTH.EQ.1)THEN
	    READ (STATUS(FIRST:LAST),110)SPLITS(2)
	  ELSE
	    READ (STATUS(FIRST:LAST),120)SPLITS(2)
	  END IF

	  FIRST=LOOP+1				! FIRST DIGIT IN YEAR
	  GO TO 310

	END IF

300	CONTINUE

C	* COMPUTE INTEGER YEAR *

310	DO 400 LOOP=FIRST,8

	IF(STATUS(LOOP:LOOP).EQ.' ')THEN	! FIND BLANK AFTER YEAR

	  LAST=LOOP-1				! LAST DIGIT IN YEAR
	  GO TO 500

	END IF

400	CONTINUE
	LAST=8

500	LENGTH=LAST-FIRST+1			! LENGTH OF YEAR FIELD

	IF(LENGTH.EQ.1)THEN
	  READ (STATUS(FIRST:LAST),110)SPLITS(3)
	ELSE
	  READ (STATUS(FIRST:LAST),120)SPLITS(3)
	END IF

	RETURN
	END
